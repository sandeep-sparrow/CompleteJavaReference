package in.github.sandeep.productApp;

public class Product {
	
	private String productCode;
	private String description;
	private double price;
	
	// getter & setter method to access private String productCode
	public String getProductCode() {
		return productCode;
	}
	public void setProductCode(String productCode) {
		this.productCode = productCode;
	}
	
	// getter & setter method to access private String description
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
	// getter & setter method to access private double price
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	


}
